const TREE_SYMBOL = '#';


const countTreesBySlopeRule = (path, slopeRule) => {
    let totalTreesEncountered = 0;
    let currentCell = 0
    let rowIndex = 0;

    path.forEach((row, index) => {
        rowIndex += slopeRule.rowDown;
        const belowRow = path[rowIndex];

        if (belowRow) {
            const totalRowPath = belowRow.length;

            if (currentCell + slopeRule.cellJump > totalRowPath) {
                currentCell = currentCell + slopeRule.cellJump - totalRowPath;
            }
            else if (currentCell + slopeRule.cellJump === totalRowPath) {
                currentCell = 0;
            }
            else {
                currentCell += slopeRule.cellJump;
            }

            const charFromBelowRow = belowRow.charAt(currentCell);

            if (charFromBelowRow === TREE_SYMBOL) {
                totalTreesEncountered++;
            }
        }
    });

    return totalTreesEncountered;
}

const moveRightByThree = countTreesBySlopeRule(tobogganPath, {
    cellJump: 3,
    rowDown: 1
});

const moveRightByOne = countTreesBySlopeRule(tobogganPath, {
    cellJump: 1,
    rowDown: 1
});

const moveRightByFive = countTreesBySlopeRule(tobogganPath, {
    cellJump: 5,
    rowDown: 1
});

const moveRightBySeven = countTreesBySlopeRule(tobogganPath, {
    cellJump: 7,
    rowDown: 1
});

const moveRightByOneDownByTwo = countTreesBySlopeRule(tobogganPath, {
    cellJump: 1,
    rowDown: 2
});

const result = moveRightByThree * moveRightByOne * moveRightByFive * moveRightBySeven * moveRightByOneDownByTwo;
